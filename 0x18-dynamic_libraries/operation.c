#include <stdio.h>

/**
 * add - a function that sums values
 * @a: first value
 * @b: second value
 * Return: the result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function that subtract values
 * @a: first value
 * @b: second value
 * Return: the result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - a function that multiple values
 * @a: first value
 * @b: second value
 * Return: the result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - a function that divides values
 * @a: first value
 * @b: second value
 * Return: the result
 */

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return(a % b);
}
